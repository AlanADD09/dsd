public class ej3 {
    public static void main(String args[]){
        System.out.println("Primer ciclo");
        for (int i = 1; i <= 15; i++) {
            System.out.println(i);
        }
        System.out.println("Segundo ciclo");
        for (int i = 15; i >= 1; i=i-2) {
            System.out.println(i);
        }
    }
}
